function get_comments_categories () {
	$db = $GLOBALS["db"];
		// get the data from the DB
	$result = mysqli_query ($db, "SELECT * FROM comment_categories");
	
	// if there was an error
	if (!$result) {
		$error = "<p>get_comments_categories (): Error while retrieving data from the DB. Please notify the administrator on support@freeurmind.net<br><strong>Error code:</strong></p>".mysqli_error($db);
		
		if ($GLOBALS["debug_mode"]==1) {
			echo show_error($error);
		}
		
		return 0;
	}
	
	// if nothing returned
	if (mysqli_num_rows($result)>0) {
		return $result;
	}
	// if nothing returned
	else {
		return 0;
	}
}
